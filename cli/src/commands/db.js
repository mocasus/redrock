const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');
const https = require('https');
const chalk = require('chalk');
const ora = require('ora');

// ── Vercel API helpers ──

function vercelApi(method, apiPath, token, body = null) {
  return new Promise((resolve, reject) => {
    const url = new URL(`https://api.vercel.com${apiPath}`);
    const options = {
      method,
      headers: {
        Authorization: `Bearer ${token}`,
        'Content-Type': 'application/json'
      },
      timeout: 15000
    };

    const req = https.request(url, options, (res) => {
      let data = '';
      res.on('data', chunk => data += chunk);
      res.on('end', () => {
        try {
          resolve({ status: res.statusCode, data: JSON.parse(data) });
        } catch {
          resolve({ status: res.statusCode, data: { raw: data } });
        }
      });
    });

    req.on('error', reject);
    req.on('timeout', () => { req.destroy(); reject(new Error('Vercel API timeout')); });

    if (body) req.write(JSON.stringify(body));
    req.end();
  });
}

async function getProjectId(token) {
  // Try to get linked project from .vercel/project.json
  const projectJson = path.join(process.cwd(), '.vercel', 'project.json');
  if (fs.existsSync(projectJson)) {
    const pj = JSON.parse(fs.readFileSync(projectJson, 'utf8'));
    if (pj.projectId) return pj.projectId;
  }

  // Try to get from vercel.json
  if (fs.existsSync('vercel.json')) {
    try {
      const vc = JSON.parse(fs.readFileSync('vercel.json', 'utf8'));
      if (vc.name) {
        const result = await vercelApi('GET', `/v9/projects?slug=${vc.name}&limit=1`, token);
        if (result.data?.projects?.length) return result.data.projects[0].id;
      }
    } catch {}
  }

  return null;
}

async function createKVStore(token, projectId) {
  // Vercel KV integration: create store and link to project
  // POST /v1/stores/kv + link to project
  const result = await vercelApi('POST', `/v1/stores/kv`, token, {
    name: `redrock-${projectId.slice(0, 8)}`,
    projectIds: [projectId]
  });

  if (result.status === 201 || result.status === 200) {
    return result.data;
  }

  // Check if already exists
  if (result.data?.error?.code === 'already_exists') {
    // List existing stores and find ours
    const list = await vercelApi('GET', `/v1/stores/kv?projectId=${projectId}`, token);
    if (list.data?.stores?.length) return list.data.stores[0];
  }

  throw new Error(result.data?.error?.message || `KV create failed (HTTP ${result.status})`);
}

function generateDbHelper(projectDir) {
  const dbPy = `"""
Redrock DB Helper — auto-generated by redrock db init

Usage:
    from api.db import db
    db.set("visits", 42)
    count = db.get("visits")
"""

import os, json, urllib.request, urllib.error

class RedrockDB:
    def __init__(self):
        self.url = os.environ.get("KV_REST_API_URL", "")
        self.token = os.environ.get("KV_REST_API_TOKEN", "")

    def _request(self, method, path, body=None):
        url = f"{self.url}{path}"
        headers = {"Authorization": f"Bearer {self.token}"}
        data = json.dumps(body).encode() if body else None
        req = urllib.request.Request(url, data=data, headers=headers, method=method)
        try:
            with urllib.request.urlopen(req, timeout=10) as resp:
                result = json.loads(resp.read())
                return result.get("result")
        except urllib.error.HTTPError as e:
            if e.code == 404:
                return None
            raise

    def set(self, key, value, ttl=None):
        """Set a key-value pair. value auto-serializes."""
        payload = {"value": json.dumps(value) if not isinstance(value, str) else value}
        if ttl:
            payload["ex"] = ttl
        return self._request("POST", f"/set/{key}", payload)

    def get(self, key, default=None):
        """Get a value by key. Auto-deserializes JSON."""
        result = self._request("GET", f"/get/{key}")
        if result is None:
            return default
        try:
            return json.loads(result)
        except (json.JSONDecodeError, TypeError):
            return result

    def delete(self, key):
        """Delete a key."""
        return self._request("DELETE", f"/del/{key}")

    def list(self, prefix=""):
        """List keys matching prefix."""
        return self._request("GET", f"/list/{prefix}") or []

    def incr(self, key, amount=1):
        """Increment a counter."""
        current = self.get(key, 0)
        if not isinstance(current, (int, float)):
            current = 0
        new_val = current + amount
        self.set(key, new_val)
        return new_val

db = RedrockDB()
`;

  // Ensure api/ directory exists
  const apiDir = path.join(projectDir, 'api');
  if (!fs.existsSync(apiDir)) fs.mkdirSync(apiDir, { recursive: true });

  fs.writeFileSync(path.join(apiDir, 'db.py'), dbPy);
}

// ── Main command ──

module.exports = async function db(action, opts = {}) {
  if (action === 'init') {
    const token = process.env.VERCEL_TOKEN;

    if (!token) {
      console.log(chalk.red('❌ VERCEL_TOKEN not set.'));
      console.log(chalk.dim('   Get it from: https://vercel.com/account/tokens'));
      console.log(chalk.dim('   Then: export VERCEL_TOKEN=<token>'));
      return;
    }

    const spinner = ora('Connecting to Vercel KV...').start();

    try {
      const projectId = await getProjectId(token);
      if (!projectId) {
        spinner.fail('No Vercel project found.');
        console.log(chalk.yellow('   Link project first:'));
        console.log(chalk.dim('   vercel link'));
        return;
      }

      spinner.text = 'Creating KV store...';
      const store = await createKVStore(token, projectId);

      if (!store || !store.url) {
        spinner.fail('Failed to create KV store.');
        console.log(chalk.yellow('   Create manually: https://vercel.com/dashboard/stores'));
        return;
      }

      spinner.text = 'Setting environment variables...';

      // Add env vars to Vercel project
      const envVars = {
        KV_URL: store.url,
        KV_REST_API_URL: store.restApiUrl || store.url,
        KV_REST_API_TOKEN: store.restApiToken || store.token,
        KV_REST_API_READ_ONLY_TOKEN: store.readOnlyToken || ''
      };

      for (const [key, value] of Object.entries(envVars)) {
        if (!value) continue;
        try {
          execSync(`vercel env add ${key} production --token ${token}`, {
            stdio: 'pipe',
            input: value
          });
        } catch {
          // env might already exist
        }
      }

      // Write local .env
      const envLines = Object.entries(envVars)
        .filter(([, v]) => v)
        .map(([k, v]) => `${k}=${v}`)
        .join('\n');

      const envPath = '.env';
      const existing = fs.existsSync(envPath) ? fs.readFileSync(envPath, 'utf8') : '';
      if (!existing.includes('KV_REST_API_URL')) {
        fs.appendFileSync(envPath, `\n# Redrock KV\n${envLines}\n`);
      }

      // Generate db.py helper
      generateDbHelper(process.cwd());

      spinner.succeed('Database ready!');
      console.log(chalk.green('\n🗄️  Vercel KV configured'));
      console.log(chalk.dim('   Env vars written to .env + Vercel'));
      console.log(chalk.dim('   Helper generated: api/db.py'));
      console.log('');
      console.log(chalk.cyan('   Usage in your bot:'));
      console.log(chalk.dim('   from api.db import db'));
      console.log(chalk.dim('   db.set("key", "value")'));
      console.log(chalk.dim('   value = db.get("key")'));

    } catch (err) {
      spinner.fail('Database setup failed.');
      console.log(chalk.red(`   ${err.message}`));

      // Fallback: generate db.py anyway with manual setup instructions
      if (err.message.includes('KV')) {
        console.log(chalk.yellow('\n📋 Manual setup:'));
        console.log(chalk.dim('   1. Go to https://vercel.com/dashboard/stores'));
        console.log(chalk.dim('   2. Create a KV Database'));
        console.log(chalk.dim('   3. Copy KV_REST_API_URL + KV_REST_API_TOKEN to .env'));
        console.log(chalk.dim('   4. Run redrock db init again'));

        // Still generate the helper
        generateDbHelper(process.cwd());
        console.log(chalk.green('\n   ✓ api/db.py generated'));
      }
    }

  } else if (action === 'migrate') {
    const to = opts.to || 'supabase';
    console.log(chalk.yellow(`🔄 Migration to ${to}`));
    console.log(chalk.dim(`   Coming in v1.2 — exports data and re-imports to ${to}`));
    console.log(chalk.dim(`   For now: use provider SDKs directly`));

  } else {
    console.log(chalk.red(`❌ Unknown action: ${action}`));
    console.log(chalk.dim('   Available: redrock db init, redrock db migrate --to <provider>'));
  }
};
